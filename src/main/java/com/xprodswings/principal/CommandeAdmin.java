    private void tablePRODUITMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePRODUITMouseClicked
        // Pour selectionner  les informations d'un champ
        double somme = 0.0;
        double totalTTC = 0.0;
        double prixTVA = 0.0;

        DefaultTableModel dtm =(DefaultTableModel)tablePRODUIT.getModel();
        int selectedIndex = tablePRODUIT.getSelectedRow();

        txtREF.setText(dtm.getValueAt(selectedIndex, 1).toString());
        txtDESIGNATION.setText(dtm.getValueAt(selectedIndex, 2).toString());
        txtDESCRIPTION.setText(dtm.getValueAt(selectedIndex, 3).toString());
        txtPRIXUHT.setText(dtm.getValueAt(selectedIndex, 4).toString());

        String idClient = dtm.getValueAt(selectedIndex, 0).toString();
        String txtREF = dtm.getValueAt(selectedIndex, 1).toString();
        String txtDESIGNATION = dtm.getValueAt(selectedIndex, 2).toString();
        String txtDESCRIPTION = dtm.getValueAt(selectedIndex, 3).toString();
        double txtPRIXUHT = Double.parseDouble(dtm.getValueAt(selectedIndex, 4).toString());

        int qte = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrez la quantité !"));

        double tot = txtPRIXUHT * qte;
        double fraisPort = Double.parseDouble(JOptionPane.showInputDialog(null,"Entrez les frais de port"));
        int TVA = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrez la tva!"));
        dtm = (DefaultTableModel)tableCOMMANDE.getModel();
 
        dtm.addRow(new Object[]
        {   
            idClient,
            txtREF,
            txtDESIGNATION,
            txtDESCRIPTION,
            txtPRIXUHT,
            qte,
            tot
            }
        );
        for(int i=0;i<tableCOMMANDE.getRowCount();i++){
            somme = fraisPort + somme + Double.parseDouble(tableCOMMANDE.getValueAt((int)i,6).toString());
            prixTVA = somme*TVA/100;
            totalTTC = somme + somme*TVA/100 ;
            }
        txtPRIXTTC.setText(Double.toString(totalTTC));
        txtQTE.setText(Integer.toString(qte));
        txtPRIXTHT.setText(Double.toString(tot));
        txtFRAISDEPORT.setText(Double.toString(fraisPort));
        txtTVA.setText(Double.toString(prixTVA));
    }//GEN-LAST:event_tablePRODUITMouseClicked

