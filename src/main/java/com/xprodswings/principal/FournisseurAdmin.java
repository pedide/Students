    private void tableUpdate(){
        int cpt;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/xprod", "root","");
            pst=con.prepareStatement("SELECT * FROM `fournisseurs`");
            ResultSet rs = null;
            try {
                rs = pst.executeQuery();
            } catch (SQLException ex) {
                Logger.getLogger(XprodDesktopAdministrateur.class.getName()).log(Level.SEVERE, null, ex);
            }

            ResultSetMetaData rsmd = rs.getMetaData();
            cpt= rsmd.getColumnCount();
            DefaultTableModel dtm =(DefaultTableModel)tableFOURNISSEUR.getModel();
            dtm.setRowCount(0);

            while(rs.next()){
                Vector vect = new Vector();
                for (int i=1;i<= cpt; i++){
                    vect.add(rs.getString("IDFOURNISSEUR"));
                    vect.add(rs.getString("RSFOUR"));
                    vect.add(rs.getString("NOMFOUR"));
                    vect.add(rs.getString("ADRESSEFOUR"));
                    vect.add(rs.getString("CPFOUR"));
                    vect.add(rs.getString("VILLEFOUR"));
                    vect.add(rs.getString("PAYSFOUR"));
                    vect.add(rs.getString("TELFOUR"));
                    vect.add(rs.getString("FAXFOUR"));
                    vect.add(rs.getString("EMAILFOUR"));
                    }
                    dtm.addRow(vect);
                }

            }catch (ClassNotFoundException ex) {
                Logger.getLogger(FournisseurAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }catch (SQLException ex) {
                Logger.getLogger(FournisseurAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnAJOUTERFOURActionPerformed

