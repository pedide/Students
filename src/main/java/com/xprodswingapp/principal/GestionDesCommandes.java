    private void tablePRODUITMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePRODUITMouseClicked
        
        double sum = 0.0;
        double totalTTC = 0.0;
        double prixTVA = 0.0;
        
        DefaultTableModel dtm = (DefaultTableModel)tablePRODUIT.getModel();
        int selectedIndex = tablePRODUIT.getSelectedRow();
        
        //Préremplir les champs de texte
        txtIDPRODUIT.setText(dtm.getValueAt(selectedIndex, 0).toString());
        txtREFPRODUIT.setText(dtm.getValueAt(selectedIndex, 1).toString());
        txtDESIGNATIONPRODUIT.setText(dtm.getValueAt(selectedIndex, 2).toString());
        txtDESCRIPTIFPRODUIT.setText(dtm.getValueAt(selectedIndex, 3).toString());
        txtPRIXUHT.setText(dtm.getValueAt(selectedIndex, 4).toString());
        
        //Ajout dans la table de commandes
        int idPRODUIT = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
        String txtREFPRODUIT = dtm.getValueAt(selectedIndex,1).toString();
        String txtDESIGNATIONPRODUIT = dtm.getValueAt(selectedIndex,2).toString();
        String txtDESCRIPTIFPRODUIT = dtm.getValueAt(selectedIndex,3).toString();
        double txtPRIXUHT = Double.parseDouble(dtm.getValueAt(selectedIndex, 4).toString());
        
        int qte = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrez la quantité !"));
        
        
        double tot = txtPRIXUHT * qte;
        double fraisPort = Double.parseDouble(JOptionPane.showInputDialog(null,"Entrez les frais de port !"));
        
        //double fraisPort = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrez les frais de port !"));
        int tva = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrez la T.V.A exemple 20% !"));
        dtm = (DefaultTableModel)tableLIGNECMD.getModel();
        
        dtm.addRow(new Object[] {
        
            idPRODUIT,
            txtREFPRODUIT,
            txtDESIGNATIONPRODUIT,
            txtDESCRIPTIFPRODUIT,
            txtPRIXUHT,
            qte,
            tot
            
        
        });

       for(double i=0; i<tableLIGNECMD.getRowCount(); i++){
    
        sum = fraisPort + sum + Double.parseDouble(tableLIGNECMD.getValueAt((int)i,6).toString());
        prixTVA = sum*tva/100;
        totalTTC = sum + sum*tva/100;     
    }
       
       txtIDPRODUIT.setText(Integer.toString(idPRODUIT));
       txtPRIXTTC.setText(Double.toString(totalTTC));
       txtQTE.setText(Integer.toString(qte));
       txtPRIXTHT.setText(Double.toString(tot));
       txtTVACMD.setText(Double.toString(prixTVA));
       
       if(fraisPort>=fraisPort2){
         txtFRAISDEPORTCMD.setText(Double.toString(fraisPort));
         fraisPort2 = fraisPort;
       } else {
         txtFRAISDEPORTCMD.setText(Double.toString(fraisPort2));

       }
       
       
    }//GEN-LAST:event_tablePRODUITMouseClicked

