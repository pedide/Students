    private void btnREGISTERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnREGISTERActionPerformed


            //Je récupere les informations de mon champs de texte
            String identifiantRG = txtIDENTIFIANTRG.getText();
            String mdpRG = txtMDPRG.getText();
            String authRG = txtAUTHRG.getText();
        
        try {
            
            Class.forName("com.mysql.cj.jdbc.Driver");//Enregistrer le driver JDBC
            con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb","root",""); // Demande au DriverManager (gestionnaire de manager) de récuperer et de stocker les infos dans la variable -> con
            pst = con.prepareStatement("INSERT INTO user (USERNAME,PASSWORD,AUTHORIZATION) VALUES (?,?,?)");
            
            pst.setString(1, identifiantRG);
            pst.setString(2, mdpRG);
            pst.setString(3, authRG);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données enregistrées");
            
            //Vide les TextFields
            txtIDENTIFIANTRG.setText("");
            txtMDPRG.setText("");
            txtAUTHRG.setText("");
   
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnREGISTERActionPerformed

