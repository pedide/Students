    private void tablePRODUITMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePRODUITMouseClicked
        //pour selectionner les informations d'un champ
        int selectedIndex = tablePRODUIT.getSelectedRow();
        
        double sum = 0.0;
        double totalTCC = 0.0;
        double prixTVA = 0.0;
        double fraisDePort2 = 0; 
        
        DefaultTableModel dtm =(DefaultTableModel)tablePRODUIT.getModel(); //on va recuperer le resultat ds un model

        //preremplir les champs de texte
        txtREFPRODUIT.setText(dtm.getValueAt(selectedIndex, 1).toString());
        txtDESIGNATIONPRODUIT.setText(dtm.getValueAt(selectedIndex, 2).toString());
        txtDESCRIPTIFPRODUIT.setText(dtm.getValueAt(selectedIndex, 3).toString());
        txtPRIXUHTPRODUIT.setText(dtm.getValueAt(selectedIndex, 4).toString());
        
        //ajouter dans la table des commandes
        String idPRODUIT = dtm.getValueAt(selectedIndex, 0).toString();
        String txtREFPRODUIT = dtm.getValueAt(selectedIndex, 1).toString();
        String txtDESIGNATIONPRODUIT = dtm.getValueAt(selectedIndex, 2).toString();
        String txtDESCRIPTIFPRODUIT = dtm.getValueAt(selectedIndex, 3).toString();
        double txtPRIXUHTPRODUIT = Double.parseDouble(dtm.getValueAt(selectedIndex, 4).toString());
        
        int qte = Integer.parseInt(JOptionPane.showInputDialog(null, "Entrez la quantité !"));
        
        double tot = txtPRIXUHTPRODUIT * qte;
        double fraisDePort = Integer.parseInt(JOptionPane.showInputDialog(null, "Entrez les frais de port !"));
        double tva = Integer.parseInt(JOptionPane.showInputDialog(null, "Entrez la TVA, par exemple 20 !"));
          
        
        dtm = (DefaultTableModel)tableCOMMANDE.getModel();    
                
        dtm.addRow(new Object[] {idPRODUIT,txtREFPRODUIT,txtDESIGNATIONPRODUIT,txtDESCRIPTIFPRODUIT,txtPRIXUHTPRODUIT,qte,tot});
        
        for(int i=0; i<tableCOMMANDE.getRowCount(); i++){
            sum = sum + Double.parseDouble(tableCOMMANDE.getValueAt((int)i,6).toString());//6 colonnes dans la table
            prixTVA = sum * tva/100;
            totalTCC = (sum + sum*tva/100)+fraisDePort;
        }
        
        //il compare les frais de port et c'est maximum qui est pris
        if(fraisDePort >= fraisDePort2){
            txtFRAISDEPORTCMD.setText(Double.toString(fraisDePort));
            fraisDePort2 = fraisDePort;
        }else{
            txtFRAISDEPORTCMD.setText(Double.toString(fraisDePort2));
        }
        
        txtPRIXTOTALTCC.setText(Double.toString(totalTCC));
        txtQTEPRODUIT.setText(Integer.toString(qte));
        txtPRIXTHT.setText(Double.toString(tot));
        txtFRAISDEPORTCMD.setText(Double.toString(fraisDePort));
        txtTVA.setText(Double.toString(prixTVA));
        
     
    }//GEN-LAST:event_tablePRODUITMouseClicked

