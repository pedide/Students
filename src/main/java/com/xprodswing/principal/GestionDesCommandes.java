    private void tablePRODUITMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePRODUITMouseClicked

        // pour selectionner les informations d'un champ
        double sum = 0.0;
        double totaleTTC = 0.0;
        double prixTVA = 0.0;
        DefaultTableModel dtm = (DefaultTableModel)tablePRODUIT.getModel();
        int selectedIndex = tablePRODUIT.getSelectedRow();

        txtREFC.setText(dtm.getValueAt(selectedIndex, 1).toString());
        textDESIGNATIONC.setText(dtm.getValueAt(selectedIndex, 2).toString());
        textDESCRIPTIFC.setText(dtm.getValueAt(selectedIndex, 3).toString());
        textPrixUHT.setText(dtm.getValueAt(selectedIndex, 4).toString());
       // ajout dans la table des commandes
       String idClient = dtm.getValueAt(selectedIndex, 0).toString();
       String textDESIGNATIONC = dtm.getValueAt(selectedIndex, 2).toString();
       String txtREFC = dtm.getValueAt(selectedIndex, 1).toString();
       String textDESCRIPTIFC =dtm.getValueAt(selectedIndex, 3).toString();
        double textPrixUHT = Double.parseDouble(dtm.getValueAt(selectedIndex, 4).toString());
        
        int qte = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrer la quantité"));
        
        Double tot = textPrixUHT * qte;
        int FraisPort = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrée les Frais de Port !"));
        int tva = Integer.parseInt(JOptionPane.showInputDialog(null,"Entrer la TVA exemple 20 !"));
        dtm = (DefaultTableModel)tableCommandes.getModel();
        dtm.addRow(new Object[]{idClient, txtREFC, textDESIGNATIONC,textDESCRIPTIFC ,textPrixUHT,qte,tot});
        
    
    
         for(int i=0 ; i<tableCommandes.getRowCount(); i++){ 
         sum= FraisPort + sum + Double.parseDouble(tableCommandes.getValueAt((int)i,5).toString());
         prixTVA = sum*tva/100;
         totaleTTC = sum + sum*tva/100 ;
      
         }                         
         txtPrixTTC.setText(Double.toString(totaleTTC));
         textPRIXTHT.setText(Double.toString(tot));
         textQTE.setText(Integer.toString(qte));
         txtFraisPort.setText(Double.toString(FraisPort));
         txtTVA.setText(Double.toString(prixTVA));
         
                       
    }//GEN-LAST:event_tablePRODUITMouseClicked

