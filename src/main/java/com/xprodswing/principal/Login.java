    private void btnLoginConnectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginConnectionActionPerformed
   String query = "select * from user where username=? and password=?";
   try{
        con =DriverManager.getConnection("jdbc:mysql://localhost/xproddb","root","");
        pst=con.prepareStatement(query);
        pst.setString(1, txtIdentifiant.getText());
        pst.setString(2, txtMdp.getText());
        rs =pst.executeQuery();
        if(rs.next()){
            JOptionPane.showMessageDialog(null,"Connexion réussie");
        }else{
        JOptionPane.showMessageDialog(null,"Identifiant ou mot de passe incorrect");
        }
   }    catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(txtIdentifiant.getText().equals("") || txtMdp.getText().equals("")){
            
       JOptionPane.showMessageDialog(null,"Entrez le nom d'utilisateur et le mot de passse","message",JOptionPane.INFORMATION_MESSAGE );
        }
   else{
       try {

           listLogin = udao.rechercherLogin(txtIdentifiant.getText(), DataEncryption.getSHA1(txtMdp.getText()) );
           // listLogin = udao.rechercherLogin(txtIdentifiant.getText(),txtMdp.getText()); Pas d'encryptage du mdp
           } catch (NoSuchAlgorithmException ex) {
           Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
       } catch (UnsupportedEncodingException ex) {
           Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
       }
       }
       
       
        if(listLogin.size()>=1){
         

           if(listLogin.get(0).getAuthorization().equalsIgnoreCase("admin")){
               
               
                   
                   //Page des administrateur
                   XprodDesktopAdmin desktopAdmin = new XprodDesktopAdmin();
                   desktopAdmin.setVisible(true);
                   desktopAdmin.afficherUser(txtIdentifiant.getText());
                   dispose();
              } 
           
           else{
               
               XprodDesktopClient desktopClient = new XprodDesktopClient();
               desktopClient.setVisible(true);
               desktopClient.afficherUser(txtIdentifiant.getText());
               dispose();
           }
           }
       
    }//GEN-LAST:event_btnLoginConnectionActionPerformed

