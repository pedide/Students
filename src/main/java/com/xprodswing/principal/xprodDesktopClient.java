    private void tableUpdate(){
        int cpt;
        try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root", "");
        pst = con.prepareStatement("SELECT * FROM produits");// préparer la requête
        ResultSet rs = pst.executeQuery(); //Exécuter la requête SQL et la  mettre dans  la variable rs (ResultSet)
        
        ResultSetMetaData rsmd = rs.getMetaData();//Va chercher les données
        cpt = rsmd.getColumnCount();
        DefaultTableModel dtm = (DefaultTableModel)TablePRODUIT.getModel();
        dtm.setRowCount(0);
       
        while(rs.next()){ 
            Vector vect = new Vector();
            
            
           for(int i=1; i<= cpt; i++) {
                vect.add(rs.getString("IDPRODUIT")); // rajouter les éléments Id dans vecteur
                vect.add(rs.getString("REF"));
                vect.add(rs.getString("DESIGNATION"));
                vect.add(rs.getString("DESCRIPTIF"));
                vect.add(rs.getString("PRIXUHT"));
            }
            
             dtm.addRow(vect);   
        }                 

        }catch (ClassNotFoundException ex) { 
            Logger.getLogger(xprodDesktop.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) { 
            Logger.getLogger(xprodDesktop.class.getName()).log(Level.SEVERE, null, ex); 
         
        }
        
    }
