    public void tableUpdate(){
        int cpt;
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root", ""); 
            pst = con.prepareStatement("SELECT * FROM user"); //preparation de la requete
            ResultSet rs = pst.executeQuery(); //execute la requete sql - ResultSet est memoire temporaire pour stocker rs
        
            ResultSetMetaData rsmd = rs.getMetaData();
            cpt = rsmd.getColumnCount();
            DefaultTableModel dtm =(DefaultTableModel)tableUSERREG.getModel(); //on va recuperer le resultat ds un model 
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector vect = new Vector(); //creer un vecteur

                for(int i=1; i<=cpt; i++){
                    vect.add(rs.getString("IDUSER"));
                    vect.add(rs.getString("USERNAME"));
                    vect.add(rs.getString("PASSWORD"));
                    vect.add(rs.getString("AUTHORIZATION"));
                }

                dtm.addRow(vect);
            }
        }catch (ClassNotFoundException ex){
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) {
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
