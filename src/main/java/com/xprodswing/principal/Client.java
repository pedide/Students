    private void tableUpdate() {
        int cpt;
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root", ""); 
            pst = con.prepareStatement("SELECT * FROM clients"); //preparation de la requete
            ResultSet rs = pst.executeQuery(); //execute la requete sql - ResultSet est memoire temporaire pour stocker rs
        
            ResultSetMetaData rsmd = rs.getMetaData();
            cpt = rsmd.getColumnCount();
            DefaultTableModel dtm =(DefaultTableModel)tableCLIENT.getModel(); //on va recuperer le resultat ds un model 
            dtm.setRowCount(0);
        
            while(rs.next()){
                Vector vect = new Vector(); //creer un vecteur

                for(int i=1; i<=cpt; i++){
                    vect.add(rs.getString("IDCL"));
                    vect.add(rs.getString("NOMCL"));
                    vect.add(rs.getString("PRENOMCL"));
                    vect.add(rs.getString("ADRESSECL"));
                    vect.add(rs.getString("CPCL"));
                    vect.add(rs.getString("VILLECL"));
                    vect.add(rs.getString("PAYSCL"));
                    vect.add(rs.getString("TELCL"));
                    vect.add(rs.getString("MOBILECL"));
                    vect.add(rs.getString("EMAILCL"));
                }

                dtm.addRow(vect);
            
            }
            
        }catch (ClassNotFoundException ex){
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) {
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

