    private void tableUpdate(){
            int cpt;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
                    con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root",""); // Demande au DriverManager (gestionnaire de manager) de récupérer et de stocker les infos dans la variable con
                    pst = con.prepareStatement("SELECT * FROM fournisseurs");
                    ResultSet rs = pst.executeQuery();
                    ResultSetMetaData rsmd = rs.getMetaData();
                    cpt = rsmd.getColumnCount();
                    DefaultTableModel dtm = (DefaultTableModel) tabFOURNISSEUR.getModel();
                    dtm.setRowCount(0);
                    
                    while(rs.next()){
                        Vector  vect = new Vector();
                        
                        for(int i = 1; i <= cpt; i++){
                            vect.add(rs.getString("IDFOUR"));
                            vect.add(rs.getString("RAISONSOCIAL"));
                            vect.add(rs.getString("NOMFOUR"));
                            vect.add(rs.getString("ADRESSEFOUR"));
                            vect.add(rs.getString("CODEPOSTALFOUR"));
                            vect.add(rs.getString("VILLEFOUR"));
                            vect.add(rs.getString("PAYSFOUR"));
                            vect.add(rs.getString("TELEPHONEFOUR"));
                            vect.add(rs.getString("FAXFOUR"));
                            vect.add(rs.getString("EMAILFOUR"));
                            
                        }
                    dtm.addRow(vect);
                    }
                            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    
    private void btnAjouterFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterFActionPerformed
        // Ajout fournisseur via le bouton "ajouter"
        String raisonSocialFour = txtRAISONSOCIAL.getText();
        String nomFour = txtNOM.getText();
        String adresseFour = txtADRESSE.getText();
        String codePostalFour = txtCODEPOSTAL.getText();
        String villeFour = txtVILLE.getText();
        String paysFour = txtPAYS.getText();
        String telephoneFour = txtTELEPHONE.getText();
        String faxFour = txtFAX.getText();
        String emailFour = txtEMAIL.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root",""); // Demande au DriverManager (gestionnaire de manager) de récupérer et de stocker les informations dans la variable con
            pst = con.prepareStatement("INSERT INTO fournisseurs(RAISONSOCIAL,NOMFOUR,ADRESSEFOUR,CODEPOSTALFOUR,VILLEFOUR,PAYSFOUR,TELEPHONEFOUR,FAXFOUR,EMAILFOUR) VALUES(?,?,?,?,?,?,?,?,?)"); //appel du "statement" contenant toutes les informations
            
            pst.setString(1, raisonSocialFour);
            pst.setString(2, nomFour);
            pst.setString(3, adresseFour);
            pst.setString(4, codePostalFour);
            pst.setString(5, villeFour);
            pst.setString(6, paysFour);
            pst.setString(7, telephoneFour);
            pst.setString(8, faxFour);
            pst.setString(9, emailFour);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données enregistrées"); // pour afficher une fenetre
            
            //Pour vider le contenu des champs après avoir ajouter un objet
            txtRAISONSOCIAL.setText("");
            txtNOM.setText("");
            txtADRESSE.setText("");
            txtCODEPOSTAL.setText("");
            txtVILLE.setText("");
            txtPAYS.setText("");
            txtTELEPHONE.setText("");
            txtFAX.setText("");
            txtEMAIL.setText("");
            
            tableUpdate();
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Fournisseur.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Fournisseur.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAjouterFActionPerformed

