    private void tableUpdate() {
        int cpt;
        try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root", "");
        pst = con.prepareStatement("SELECT * FROM fournisseurs");// préparer la requête
        ResultSet rs = pst.executeQuery(); //Exécuter la requête SQL et la  mettre dans  la variable rs (ResultSet)
        
        ResultSetMetaData rsmd = rs.getMetaData();//Va chercher les données
        cpt = rsmd.getColumnCount();
        DefaultTableModel dtm = (DefaultTableModel)tableFournisseurs.getModel();
        dtm.setRowCount(0);
       
        while(rs.next()){ 
            Vector vect = new Vector();
            
            
           for(int i=1; i<= cpt; i++) {
                vect.add(rs.getString("IDFOUR")); // rajouter les éléments Id dans vecteur
                    vect.add(rs.getString("RAISONSOCIALEFOUR"));
                 vect.add(rs.getString("NOMFOUR"));
                vect.add(rs.getString("ADRESSEFOUR"));
                vect.add(rs.getString("CODEPOSTALFOUR"));
                vect.add(rs.getString("VILLEFOUR"));
                vect.add(rs.getString("PAYSFOUR"));
                vect.add(rs.getString("TELFOUR"));
                  vect.add(rs.getString("FAXFOUR"));
                     vect.add(rs.getString("EMAILFOUR"));
                
                
            }
            
             dtm.addRow(vect);   
        }                 

        }catch (ClassNotFoundException ex) { 
            Logger.getLogger(xprodDesktopAdministrateur.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) { 
            Logger.getLogger(xprodDesktopAdministrateur.class.getName()).log(Level.SEVERE, null, ex); 
         
        }
        
        
    }


