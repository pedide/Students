    private void tableUpdate() {            
        
     
            
        try {
            int cpt;
            
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            // enregistrer le driver JDBC
            con=DriverManager.getConnection("jdbc:mysql://localhost/xproddb","root","");
            
        
            
            pst=con.prepareStatement("SELECT * FROM fournisseurs");
            
            
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            cpt =rsmd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)tableFournisseur.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                Vector vect = new Vector ();
                
                for (int i=1; i<= cpt; i++ ){
                    vect.add(rs.getString("idfournisseur"));
                    vect.add(rs.getString("RAISONSOCIAL"));   
                    vect.add(rs.getString("NOM"));
                    vect.add(rs.getString("ADRESSE"));
                    vect.add(rs.getString("CODEPOSTALE"));
                    vect.add(rs.getString("VILLE"));
                    vect.add(rs.getString("PAYS"));
                    vect.add(rs.getString("TEL"));
                    vect.add(rs.getString("FAX"));
                    vect.add(rs.getString("EMAIL"));
                    
                    
                    
                }
                
                dtm.addRow(vect);
            }
            
        
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(XprodDesktopAdministrateur.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(XprodDesktopAdministrateur.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }






