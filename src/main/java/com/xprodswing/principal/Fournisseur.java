    private void btnAJOUTERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAJOUTERActionPerformed
                                                  
            // Ajout fournisseur, je récupère les champs de textE
            
            String raisonSocialeFour = txtRAISONSOCIALE.getText();
            String nomFour = txtNOM.getText();
            String adresseFour = txtADRESSE.getText();
            String codePostalFour = txtCODEPOSTAL.getText();
            String villeFour = txtVILLE.getText();
                String paysFour = txtPAYS.getText();
            String telFour = txtTEL.getText();
            String faxFour = txtFAX.getText();
        String emailFour = txtEMAIL.getText();
            
            try {
                 Class.forName("com.mysql.cj.jdbc.Driver");//se connecter au driver
                 //con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb","root","");
            
                 pst = con.prepareStatement("INSERT INTO fournisseurs(RAISONSOCIALEFOUR,NOMFOUR,ADRESSEFOUR, CODEPOSTALFOUR, VILLEFOUR, PAYSFOUR, TELFOUR, FAXFOUR, EMAILFOUR)"
                        +"VALUES(?,?,?,?,?,?,?,?,?)");
             
            
                 pst.setString(1, raisonSocialeFour);
                 pst.setString(2, nomFour);
                 pst.setString(3, adresseFour);
                 pst.setString(4, codePostalFour);
                 pst.setString(5, villeFour);
                 pst.setString(6, paysFour);
                 pst.setString(7, telFour);
                 pst.setString(8, faxFour);
                 pst.setString(9, emailFour);
           
            
        
                JOptionPane.showMessageDialog(this, "Données enregistrées !");
                pst.executeUpdate();
                //Je vide le contenu des champs remplis
            
                txtRAISONSOCIALE.setText("");
                txtNOM.setText("");
                txtADRESSE.setText("");
                txtCODEPOSTAL.setText("");
                txtVILLE.setText("");
                txtTEL.setText("");
                txtFAX.setText("");
                txtPAYS.setText("");

                //actualiser la table
                 tableUpdate();
        
                } catch (SQLException ex) {     
                Logger.getLogger(Fournisseur.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                Logger.getLogger(Fournisseur.class.getName()).log(Level.SEVERE, null, ex);
            }     
    }//GEN-LAST:event_btnAJOUTERActionPerformed

